//华工考研真题

#include<cstdio>
#include<iostream>

using namespace std;

int main(){

	double L;
	scanf("%lf", &L);

	
	if ( L <= 100000 )
	{
		printf("%.2f\n", L * 0.1);
	}
	else if ( L > 100000 && L <= 200000 )
	{
		printf("%.2f\n", 100000 * 0.1 + (L - 100000) * 0.075);
	}
	else if ( L > 200000 && L <= 400000 )
	{
		printf("%.2f\n", 100000 * 0.1 + 100000 * 0.075 + (L - 200000) * 0.05);
	}
	else if ( L > 400000 && L <= 600000 )
	{
		printf("%.2f\n", 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (L - 400000) * 0.03);
	}
	else if ( L > 600000 && L <= 1000000 )
	{
		printf("%.2f\n", 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + (L - 600000) * 0.015);
	}
	else
	{
		printf("%.2f\n", 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + 400000 * 0.015 + (L - 1000000) * 0.01);
	}


	system("pause");
	return 0;
}
